        if credor_valor and nome_credor and not re.search(anti_regex,credor_valor) and len(credor_valor) < 150:
            if i != len(matchs_credor_valor) - 1:
                valor_str_credor = valor_str_credor.group(0)
                print(credor_valor , " ======" , nome_credor , " ======" , valor_str_credor)
