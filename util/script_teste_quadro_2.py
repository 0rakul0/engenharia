                    novo_credor.data = credor.data
                    novo_credor.fonte_dado = credor.fonte_dado
                    novo_credor.tipo_moeda = novo_credor.tipo_moeda
                    novo_credor.nome = nome_credor
                    novo_credor.valor = valor

                    quadro_service.salvar(novo_credor,tag="FALENCIAS")
