regex ="(.*?[R|US]\$\s*\d+[\.|\,]?\d*)"

for id in lista_id:
    credor = quadro_service.dao.get_por_id(id)

    matchs_credor_valor = re.split(regex,credor.nome)

    for i,credor_valor in enumerate(matchs_credor_valor):
        nome_credor = re.sub("(R|US)\$\s*\d*(\.|\,)?\d*","",credor_valor).strip()
